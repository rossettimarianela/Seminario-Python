#  Escribe un programa que solicite
#  al usuario su año de nacimiento y muestre en qué
# año cumplirá 18, 21 y 100 años.

birth_year = int(input("Ingrese su año de nacimiento: "))
print(f'Cumplirá 18 en: {birth_year+18}')
print(f'Cumplirá 21 en: {birth_year+21}')
print(f'Cumplirá 100 en: {birth_year+100}')